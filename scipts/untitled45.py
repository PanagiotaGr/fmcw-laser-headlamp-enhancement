def line_to_support(points: np.ndarray, rho: float, theta: float, dist_thresh: float):
    """Βρίσκει ποια σημεία υποστηρίζουν μια candidate Hough line."""
    x = points[:, 0]
    y = points[:, 1]
    d = np.abs(x * np.cos(theta) + y * np.sin(theta) - rho)
    return np.where(d < dist_thresh)[0]

def hough_2d(points: np.ndarray, n_rho: int = 200, n_theta: int = 180, rho_max: float = 150):
    """Κλασικός 2D Hough Transform για ευθείες σε normal form."""
    thetas = np.linspace(-np.pi / 2, np.pi / 2, n_theta)
    rhos = np.linspace(-rho_max, rho_max, n_rho)
    acc = np.zeros((n_rho, n_theta), dtype=float)
    drho = rhos[1] - rhos[0]

    cos_t = np.cos(thetas)
    sin_t = np.sin(thetas)

    for x, y in points:
        rho_vals = x * cos_t + y * sin_t
        idx = np.floor((rho_vals - rhos[0]) / drho).astype(int)
        valid = (idx >= 0) & (idx < n_rho)
        acc[idx[valid], np.arange(n_theta)[valid]] += 1.0

    # 3x3 mean filter όπως περιγράφεται στο paper
    acc = uniform_filter(acc, size=3)
    return acc, rhos, thetas

def peak_candidates(acc: np.ndarray, rhos: np.ndarray, thetas: np.ndarray, top_k: int = 8):
    """Επιστρέφει τις ισχυρότερες κορυφές του Hough space."""
    flat_idx = np.argpartition(acc.ravel(), -top_k)[-top_k:]
    flat_idx = flat_idx[np.argsort(acc.ravel()[flat_idx])[::-1]]

    out = []
    for idx in flat_idx:
        ri, ti = np.unravel_index(idx, acc.shape)
        out.append((rhos[ri], thetas[ti], acc[ri, ti]))
    return out

def projection_candidates(points_2d: np.ndarray, dist_thresh: float = 2.5, top_k: int = 8):
    """
    Από μια προβολή 2D:
      Hough -> peaks -> supporting points
    """
    acc, rhos, thetas = hough_2d(points_2d)
    peaks = peak_candidates(acc, rhos, thetas, top_k=top_k)

    candidates = []
    for rho, theta, score in peaks:
        support_idx = line_to_support(points_2d, rho, theta, dist_thresh)
        if len(support_idx) >= 4:
            candidates.append({
                "rho": rho,
                "theta": theta,
                "score": score,
                "support": set(support_idx.tolist())
            })

    return candidates, acc, rhos, thetas

def and_logic_fusion(points_xyz, dist_thresh: float = 2.5, min_common: int = 6, top_k: int = 8):
    """
    Paper-aligned AND-logic fusion:
    υποψήφια segments γίνονται valid μόνο αν υποστηρίζονται
    κοινά και στις 3 προβολές.
    """
    x, y, t = points_xyz

    pts_xy = np.column_stack([x, y])
    pts_xt = np.column_stack([x, t])
    pts_yt = np.column_stack([y, t])

    cand_xy, acc_xy, rhos_xy, thetas_xy = projection_candidates(pts_xy, dist_thresh, top_k)
    cand_xt, acc_xt, rhos_xt, thetas_xt = projection_candidates(pts_xt, dist_thresh, top_k)
    cand_yt, acc_yt, rhos_yt, thetas_yt = projection_candidates(pts_yt, dist_thresh, top_k)

    valid_segments = []
    for a in cand_xy:
        for b in cand_xt:
            for c in cand_yt:
                common = a["support"] & b["support"] & c["support"]
                if len(common) >= min_common:
                    valid_segments.append(sorted(list(common)))

    # Αφαίρεση διπλοτύπων
    uniq = []
    seen = set()
    for seg in valid_segments:
        key = tuple(seg)
        if key not in seen:
            seen.add(key)
            uniq.append(seg)

    aux = {
        "xy": (acc_xy, rhos_xy, thetas_xy),
        "xt": (acc_xt, rhos_xt, thetas_xt),
        "yt": (acc_yt, rhos_yt, thetas_yt),
    }
    return uniq, aux

