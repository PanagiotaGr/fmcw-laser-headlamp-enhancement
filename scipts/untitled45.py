# -----------------------------------------------------------------------------
# 0. Αναπαραγωγιμότητα
# -----------------------------------------------------------------------------
np.random.seed(42)

# -----------------------------------------------------------------------------
# 1. Παράμετροι συστήματος από το paper
# -----------------------------------------------------------------------------
c   = 3e8          # ταχύτητα φωτός (m/s)
fc  = 193.4e12     # optical carrier frequency (Hz)
B   = 10e9         # FMCW sweep bandwidth (Hz)
T   = 10e-6        # chirp period (s)
mu  = B / T        # chirp slope (Hz/s)
lam = c / fc       # optical wavelength (m)
Rb  = 1e9          # bit rate (bps)

# Στα περισσότερα scripts αναπαραγωγής χρησιμοποιούμε operating SNR = 10 dB
SNR_dB = 10.0
gamma  = 10 ** (SNR_dB / 10.0)

# -----------------------------------------------------------------------------
# 2. METRIC 1 — Ranging accuracy με CRLB από Eq. (7)
# -----------------------------------------------------------------------------
# Από το paper:
# var(tau_hat) >= c^2 / (4 B^2) * 3 / (8 pi^2 gamma M T_c^2)
#
# Αν R = c * tau / 2, τότε:
# sigma_R = (c / 2B) * sqrt( 3 / (8 pi^2 gamma M T_c^2) )
#
# Με T_c = M T, μπορούμε να λύσουμε ανάποδα ως προς M για να πετύχουμε
# περίπου 3.8 cm, όπως αναφέρει το paper.

sigma_target = 0.038  # 3.8 cm σε μέτρα

M_cubed = (3 * c**2) / (32 * np.pi**2 * gamma * B**2 * T**2 * sigma_target**2)
M = int(round(M_cubed ** (1 / 3)))
Tc = M * T

sigma_R = (c / (2 * B)) * np.sqrt(3 / (8 * np.pi**2 * gamma * M * Tc**2))

# Καμπύλη CRLB vs SNR
snr_db_axis = np.linspace(0, 20, 400)
snr_lin_axis = 10 ** (snr_db_axis / 10.0)
sigma_R_axis = (c / (2 * B)) * np.sqrt(
    3 / (8 * np.pi**2 * snr_lin_axis * M * Tc**2)
)

# -----------------------------------------------------------------------------
# 3. METRIC 2 — 1 Gbps DPSK / DBPSK
# -----------------------------------------------------------------------------
# Το paper περιγράφει differential phase coding με Δφ ∈ {0, π}
# και symbol duration T_s = 1 / R_b.
# Άρα bits per chirp = R_b * T

Ns = int(Rb * T)

def ber_dbpsk_theory(gamma_lin: np.ndarray) -> np.ndarray:
    """Θεωρητικό BER για noncoherent DBPSK σε AWGN."""
    return 0.5 * np.exp(-gamma_lin)

def required_snr_db_for_ber(target_ber: float = 1e-6) -> float:
    """Υπολογισμός του απαιτούμενου SNR (dB) για δεδομένο BER στόχο."""
    gamma_req = -np.log(2 * target_ber)
    return 10 * np.log10(gamma_req)

def sim_dbpsk_ber(snr_db_values, n_bits: int = 200_000) -> np.ndarray:
    """
    Bit-level Monte Carlo simulation για DBPSK:
      bits -> differential encoding -> AWGN -> differential detection -> BER
    """
    out = []

    for snr_db in snr_db_values:
        g = 10 ** (snr_db / 10.0)
        sigma = 1 / np.sqrt(2 * g)
        bits = np.random.randint(0, 2, n_bits)

        # Differential encoding:
        # bit=1 -> phase jump pi, bit=0 -> no phase change
        phase = np.zeros(n_bits + 1)
        for i, b in enumerate(bits, start=1):
            phase[i] = (phase[i - 1] + (np.pi if b else 0.0)) % (2 * np.pi)

        tx = np.exp(1j * phase)

        noise = sigma * (
            np.random.randn(n_bits + 1) + 1j * np.random.randn(n_bits + 1)
        ) / np.sqrt(2)
        rx = tx + noise

        metric = np.real(rx[1:] * np.conj(rx[:-1]))
        bits_hat = (metric < 0).astype(int)
        ber = np.mean(bits_hat != bits)

        # Αν το BER βγει 0 στο finite simulation, βάζουμε lower floor
        out.append(max(ber, 1 / (n_bits + 1)))

    return np.array(out)

snr_ber_pts = np.arange(0, 16, 1)
ber_theory_pts = ber_dbpsk_theory(10 ** (snr_ber_pts / 10.0))
